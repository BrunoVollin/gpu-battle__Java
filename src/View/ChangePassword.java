    private void jButtonChangeActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonChangeActionPerformed
        ProfileDAO profD = new ProfileDAO();
        String name = jTextFieldNomeEntrar.getText();
        String oldPass = jPasswordFieldAntiga.getText();
        String newPass = jPasswordFieldNova.getText();
        Profile prof = new Profile(name, oldPass);

        if(profD.checkProfile(prof)) {
            if(profD.atualizarProfile(prof, newPass)){
                JOptionPane.showMessageDialog(null, "Atualizado");
            } else {
                JOptionPane.showMessageDialog(null, "Tivemos um problema ao atualizar");
            }
        } else {
            JOptionPane.showMessageDialog(null, "n√£o existe seu perfil");
        }
        this.setVisible(false);
    }//GEN-LAST:event_jButtonChangeActionPerformed

